// ---------------------------------------------------------------------------
// File: app_spacing.dart
// Description:
// Defines a centralized set of spacing constants used throughout the app.
//
// These constants help maintain consistent spacing, padding, and margin
// values across all widgets and layouts, improving design uniformity.
//
// This kind of structure is common in scalable UI systems — instead of
// hardcoding arbitrary numbers for padding or margin, developers rely on
// these standard values to keep spacing consistent.
//
// ---------------------------------------------------------------------------

class AppSpacing {
  /// Extra small spacing value (4.0).
  static const double xs = 4.0;

  /// Small spacing value (8.0).
  static const double sm = 8.0;

  /// Medium spacing value (16.0).
  static const double md = 16.0;

  /// Large spacing value (24.0).
  static const double lg = 24.0;

  /// Extra large spacing value (32.0).
  static const double xl = 32.0;

  /// Double extra large spacing value (48.0).
  static const double xxl = 48.0;

  /// Triple extra large spacing value (64.0).
  static const double xxxl = 64.0;
}
